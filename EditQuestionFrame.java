    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
if(validateInput()==false)
{
    JOptionPane.showMessageDialog(null,"Please fill all the input fields","Blank Input!",JOptionPane.ERROR_MESSAGE);
    return;
}
      String optionName=options.get(correctOption);
           QuestionPojo pjo=new QuestionPojo(newExam.getExamId(),qno,question,option1,option2,option3,option4,optionName,newExam.getLanguage());
           qstore.addQuestion(pjo);
           clearAll();
           lblTitle.setText((newExam.getTotalQuestions()-qno)+"Question Remaining");
           qno++;
           if(qno>newExam.getTotalQuestions())
           {
               disableAll();
               JOptionPane.showMessageDialog(null,"Your Question have been Edited.\nPress the Done Button to add it to the Database","Exam Edited",JOptionPane.INFORMATION_MESSAGE);
           }
           else
               lblQno.setText("Question no:"+qno);
  QuestionPojo questions=qstore.getQuestion(pos);
        pos++;
    if(pos>=qstore.getCount())
    {
        pos=0;
       
    }
    
    if(qno<qstore.getCount())
        qno=1;
    lblQno.setText("Question no:"+qno);
    showQuestions();
    
    }//GEN-LAST:event_btnNextActionPerformed

    private void btnDoneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDoneActionPerformed
        if(qno<=newExam.getTotalQuestions()){
            int remainingQuestions=newExam.getTotalQuestions()-qno+1;
            JOptionPane.showMessageDialog(null,"You Have Still"+remainingQuestions+"left","Cannot add Exam",JOptionPane.ERROR_MESSAGE);
            return;
         }
        try{
            ExamDAO.addExam(newExam);
            QuestionDAO.updateQuestions(qstore);
            JOptionPane.showMessageDialog(null,"Questions have been Successfully Added to the DataBase","Success",JOptionPane.INFORMATION_MESSAGE);
            AdminOptionsFrame admin=new AdminOptionsFrame();
            admin.setVisible(true);
            this.dispose();
        }
        catch(SQLException ex)
        {
           ex.printStackTrace();
        }
    }//GEN-LAST:event_btnDoneActionPerformed

