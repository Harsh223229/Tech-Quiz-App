public void loadQuestions()
{
try
{
    ArrayList<QuestionPojo> questionList=QuestionDAO.getQuestionByExamId(newExam.getExamId());
    for(QuestionPojo obj:questionList)
    {
        qstore.addQuestion(obj);
    }
}
catch(SQLException sq)
            {
            sq.printStackTrace();
            }
}
